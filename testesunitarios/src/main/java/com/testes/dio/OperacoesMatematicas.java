package com.testes.dio;

public class OperacoesMatematicas {
    
    public int somar(int a, int b){
        return a+b;
    }

    public int subtracao(int a, int b){
        return a-b;
    }

    public double divisao(int a, int b){
        return a/b;
    }

    public double multiplicacao(int a, int b){
        return a*b;
    }

}
